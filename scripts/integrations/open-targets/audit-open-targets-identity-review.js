#!/usr/bin/env node
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs';
import { dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..', '..', '..');
const SNAPSHOT_PATH = resolve(ROOT, 'src/data/generatedOpenTargetsSnapshot.js');
const OUT_MD = resolve(ROOT, 'docs/OPEN_TARGETS_IDENTITY_REVIEW.md');
const CHECK = process.argv.includes('--check');

const ACCEPTED_DECISIONS = new Set(['accepted_for_context_import']);

function readSnapshot(filePath) {
  const text = readFileSync(filePath, 'utf8');
  const match = text.match(/const\s+GENERATED_OPEN_TARGETS_SNAPSHOT\s*=\s*Object\.freeze\(([\s\S]*?)\);\s*$/);
  if (!match) throw new Error(`Could not find GENERATED_OPEN_TARGETS_SNAPSHOT in ${filePath}`);
  return JSON.parse(match[1]);
}

function decisionFor(row) {
  return row.identityReviewDecision || 'unreviewed';
}

function contextCount(snapshot, chemblId) {
  return (snapshot.contextByChemblId?.[chemblId] || []).length;
}

function validate(snapshot) {
  const errors = [];
  const mapped = (snapshot.crosswalk || []).filter(row => row.chemblId);
  const accepted = mapped.filter(row => ACCEPTED_DECISIONS.has(decisionFor(row)));
  const nonAccepted = mapped.filter(row => !ACCEPTED_DECISIONS.has(decisionFor(row)));
  const combinationRows = (snapshot.crosswalk || []).filter(row => row.matchStatus === 'requires_manual_combination_review');

  for (const row of accepted) {
    for (const field of ['identityReviewedBy', 'identityReviewedAt', 'identityReviewRationale']) {
      if (!row[field]) errors.push(`${row.medcheckName}: accepted mapping missing ${field}`);
    }
    if (row.identityReviewRequired === true) {
      errors.push(`${row.medcheckName}: accepted mapping should not still require identity review`);
    }
  }

  for (const row of nonAccepted) {
    const facts = contextCount(snapshot, row.chemblId);
    if (facts > 0) {
      errors.push(`${row.medcheckName}: non-accepted mapping ${row.chemblId} exposes ${facts} context fact(s)`);
    }
  }

  for (const row of combinationRows) {
    if (row.chemblId) errors.push(`${row.medcheckName}: combination/context row should not map directly to one ChEMBL molecule`);
    if (row.combinationProductAuthority !== 'diognosis') errors.push(`${row.medcheckName}: combination/context row missing Diognosis authority marker`);
  }

  return {
    errors,
    mapped,
    accepted,
    nonAccepted,
    combinationRows,
    summary: {
      schemaVersion: 1,
      release: snapshot.release || snapshot.summary?.release || null,
      mappedRows: mapped.length,
      acceptedForContextImport: accepted.length,
      nonAcceptedMappedRows: nonAccepted.length,
      combinationDiognosisAuthorityRows: combinationRows.length,
      contextFactsOnAcceptedMappings: accepted.reduce((sum, row) => sum + contextCount(snapshot, row.chemblId), 0),
      generatedBy: 'scripts/integrations/open-targets/audit-open-targets-identity-review.js',
    },
  };
}

function cell(value) {
  return String(value == null ? '' : value).replace(/\|/g, '\\|').replace(/\n/g, ' ');
}

function renderMarkdown(report) {
  const acceptedRows = report.accepted.map(row => `| ${[
    row.medcheckName,
    row.chemblId,
    row.openTargetsName,
    row.identityReviewedBy,
    row.identityReviewedAt,
    row.identityReviewRationale,
    contextCount({ contextByChemblId: report.snapshotContextByChemblId }, row.chemblId),
  ].map(cell).join(' | ')} |`).join('\n') || '| none | none | none | none | none | none | 0 |';

  const combinationRows = report.combinationRows.map(row => `| ${[
    row.medcheckName,
    row.medcheckClass,
    row.matchReason,
  ].map(cell).join(' | ')} |`).join('\n') || '| none | none | none |';

  return `# Open Targets Identity Review

Generated by \`scripts/integrations/open-targets/audit-open-targets-identity-review.js\`.

This is a Diognosis data identity review for context-only Open Targets import. It is not a licensed professional clinical evidence review, and it does not promote any external fact into risk scoring.

## Summary

| Metric | Count |
| --- | ---: |
| Mapped rows | ${report.summary.mappedRows} |
| Accepted for context import | ${report.summary.acceptedForContextImport} |
| Non-accepted mapped rows | ${report.summary.nonAcceptedMappedRows} |
| Combination/context rows kept under Diognosis authority | ${report.summary.combinationDiognosisAuthorityRows} |
| Context facts on accepted mappings | ${report.summary.contextFactsOnAcceptedMappings} |

Open Targets release: ${report.summary.release || 'not specified'}

## Accepted Parent-Molecule Mappings

| Diognosis substance | ChEMBL/Open Targets ID | Open Targets name | Reviewed by | Reviewed at | Rationale | Context facts |
| --- | --- | --- | --- | --- | --- | ---: |
${acceptedRows}

## Combination / Context Rows

These rows are intentionally not mapped to a single Open Targets drug entity because Open Targets drug pages are molecule-centric and Diognosis keeps combination/context logic authoritative.

| Diognosis substance | Diognosis class | Reason |
| --- | --- | --- |
${combinationRows}

## Completion Checks

- Accepted mappings require reviewer, date, and rationale.
- Accepted mappings must not still be marked as needing identity review.
- Non-accepted mapped rows must not expose context facts.
- Combination/context rows must remain unmapped and marked as Diognosis authority.
`;
}

function writeIfChanged(filePath, content) {
  if (existsSync(filePath) && readFileSync(filePath, 'utf8') === content) return false;
  if (CHECK) throw new Error(`${filePath.replace(`${ROOT}/`, '')} is stale. Run npm run audit:open-targets-identity.`);
  mkdirSync(dirname(filePath), { recursive: true });
  writeFileSync(filePath, content, 'utf8');
  return true;
}

try {
  const snapshot = readSnapshot(SNAPSHOT_PATH);
  const report = validate(snapshot);
  const docReport = {
    ...report,
    snapshotContextByChemblId: snapshot.contextByChemblId || {},
  };
  if (report.errors.length) throw new Error(report.errors.join('\n'));
  const wrote = writeIfChanged(OUT_MD, renderMarkdown(docReport));
  console.log(JSON.stringify({
    ok: true,
    check: CHECK,
    wrote: { openTargetsIdentityReviewDoc: wrote },
    summary: report.summary,
  }, null, 2));
} catch (error) {
  console.error(error.message);
  process.exit(1);
}
